// Search functionality for main page
function filterMembers() {
    const input = document.getElementById('searchInput');
    if (!input) return;

    const filter = input.value.toUpperCase();
    const icons = document.getElementsByClassName('member-icon');

    for (let i = 0; i < icons.length; i++) {
        const name = icons[i].getAttribute('data-name');
        if (name.toUpperCase().indexOf(filter) > -1) {
            icons[i].style.display = "";
        } else {
            icons[i].style.display = "none";
        }
    }
}

// Load fan sites for a specific member
function loadFansites() {
    const urlParams = new URLSearchParams(window.location.search);
    const memberKey = urlParams.get('member');

    if (memberKey && fansitesData[memberKey]) {
        const member = fansitesData[memberKey];
        displayFansites(member);
    } else {
        window.location.href = 'index.html';
    }
}

// Display fan sites
function displayFansites(member) {
    document.title = `${member.name} Fan Sites - tripleS`;

    const iconElement = document.getElementById('memberIcon');
    const nameElement = document.getElementById('memberName');

    if (iconElement) {
        iconElement.style.background = member.gradient;
        iconElement.textContent = member.initials;
    }

    if (nameElement) {
        nameElement.textContent = member.name;
    }

    const listElement = document.getElementById('fansitesList');
    if (listElement && member.fansites) {
        listElement.innerHTML = member.fansites.map(fansite => `
            <a href="https://twitter.com/${fansite.handle.replace('@', '')}" target="_blank" class="fansite-card">
                <div class="fansite-info">
                    <div class="fansite-name">${fansite.name}</div>
                    <div class="fansite-handle">${fansite.handle}</div>
                </div>
                <div class="fansite-arrow">â†’</div>
            </a>
        `).join('');
    }
}

